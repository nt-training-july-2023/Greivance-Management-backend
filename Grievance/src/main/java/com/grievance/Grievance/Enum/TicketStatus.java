package com.grievance.Grievance.Enum;

/**
 * Enumeration representing the status of a ticket.
 * 
 * This enum defines three status values: Open, Being_Addressed, and
 * Resolved. 
 * 
 */
public enum TicketStatus {

	/**
	 * The ticket is open and has not been addressed yet.
	 */
	Open,

	/**
	 * The ticket is being addressed by the support team.
	 */
	Being_Addressed,

	/**
	 * The ticket has been resolved and the issue is resolved.
	 */
	Resolved
}
